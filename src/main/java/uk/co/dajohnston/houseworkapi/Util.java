package uk.co.dajohnston.houseworkapi;

public class Util {

  public int add(int x, int y) {
    return x + y;
  }

  public int subtract(int x, int y) {
    return x - y;
  }

  public int multiply(int x, int y) {
    return x * y;
  }

  public int divide(int x, int y) {
    return x / y;
  }

  public int power(int x, int y) {
    return x ^ y;
  }
}
